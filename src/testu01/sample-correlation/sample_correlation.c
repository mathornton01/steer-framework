// =================================================================================================
//  Helper: extract uniform [0,1) from bitstream
// =================================================================================================
static double ExtractUniform(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < SCORR_BITS_PER_VALUE; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return (double)value / 4294967296.0;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tTestU01_SampleCorrelationPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t n = 0;
    double mean = 0.0;
    double sumSq = 0.0;
    double sumCross = 0.0;
    double variance = 0.0;
    double r = 0.0;
    double z = 0.0;
    double pval = 0.0;
    uint64_t i;
    double* values = NULL;

    // Number of uniform values we can extract
    n = privateData->bitstreamLength / SCORR_BITS_PER_VALUE;

    if (n < 10)
    {
        *passed = false;
        return result;
    }

    // Allocate values array
    result = STEER_AllocateMemory(n * sizeof(double), (void**)&values);
    if (result != STEER_RESULT_SUCCESS) return result;

    // Extract all uniform values
    for (i = 0; i < n; i++)
        values[i] = ExtractUniform(bitstreamBuffer, i * SCORR_BITS_PER_VALUE);

    // Compute sample mean
    mean = 0.0;
    for (i = 0; i < n; i++)
        mean += values[i];
    mean /= (double)n;

    // Compute sample variance and lag-1 autocovariance
    sumSq = 0.0;
    sumCross = 0.0;
    for (i = 0; i < n - 1; i++)
    {
        double di = values[i] - mean;
        double di1 = values[i + 1] - mean;
        sumSq += di * di;
        sumCross += di * di1;
    }
    // Add the last term to sumSq
    {
        double dn = values[n - 1] - mean;
        sumSq += dn * dn;
    }

    variance = sumSq / (double)n;

    if (variance < 1e-30)
    {
        // Degenerate case — essentially constant sequence
        *passed = false;
        STEER_FreeMemory((void**)&values);
        return result;
    }

    // Lag-1 autocorrelation
    r = (sumCross / (double)(n - 1)) / variance;
    privateData->correlation = r;

    // Under H0, r ~ Normal(0, 1/n) approximately
    z = r * sqrt((double)n);
    privateData->zScore = z;

    // Two-sided p-value: P = 2 * (1 - Phi(|z|))
    pval = cephes_erfc(fabs(z) / sqrt(2.0));
    privateData->probabilityValue = pval;

    *passed = (pval >= privateData->significanceLevel);

    STEER_FreeMemory((void**)&values);
    return result;
}

