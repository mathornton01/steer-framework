// =================================================================================================
//  Helper: extract 32-bit integer from bitstream at given bit offset
// =================================================================================================
static uint32_t ExtractBits(uint8_t* buffer, uint64_t bitOffset, int numBits)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < numBits; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  Helper: compute permutation rank (Lehmer code) for a tuple of t values
//  Returns a value in [0, t!-1]
// =================================================================================================
static int PermutationRank(double* values, int t)
{
    int rank = 0;
    int factorial = 1;
    int i, j;

    // Compute (t-1)!
    for (i = 1; i < t; i++)
        factorial *= i;

    for (i = 0; i < t - 1; i++)
    {
        int count = 0;
        for (j = i + 1; j < t; j++)
        {
            if (values[j] < values[i])
                count++;
        }
        rank += count * factorial;
        factorial /= (t - 1 - i);
    }
    return rank;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tTestU01_CollisionPermutPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t maxTuples;
    uint64_t n;
    uint64_t collisions = 0;
    uint64_t* cellCounts = NULL;
    double* values = NULL;
    double expectedCollisions;
    double variance;
    double zScore;
    uint64_t i;
    uint64_t j;

    uint64_t t = privateData->tupleSize;

    // Compute k = t!
    uint64_t k = 1;
    for (j = 2; j <= t; j++)
        k *= j;

    // Determine how many overlapping tuples we can extract
    // Need at least (n + t - 1) values total for n overlapping tuples
    maxTuples = privateData->bitstreamLength / CPERM_BITS_PER_VAL;
    if (maxTuples >= t)
        maxTuples = maxTuples - t + 1;
    else
        maxTuples = 0;
    n = (maxTuples > CPERM_N) ? CPERM_N : maxTuples;

    // Allocate cell counts
    result = STEER_AllocateMemory(k * sizeof(uint64_t), (void**)&cellCounts);
    if (result != STEER_RESULT_SUCCESS)
        return result;
    memset(cellCounts, 0, k * sizeof(uint64_t));

    // Allocate tuple buffer
    result = STEER_AllocateMemory(t * sizeof(double), (void**)&values);
    if (result != STEER_RESULT_SUCCESS)
    {
        STEER_FreeMemory((void**)&cellCounts);
        return result;
    }

    // Process each overlapping tuple
    for (i = 0; i < n; i++)
    {
        int rank;

        for (j = 0; j < t; j++)
        {
            uint32_t raw = ExtractBits(bitstreamBuffer,
                                       (i + j) * CPERM_BITS_PER_VAL,
                                       CPERM_BITS_PER_VAL);
            values[j] = (double)raw / 4294967296.0;
        }

        rank = PermutationRank(values, (int)t);
        if (rank >= 0 && (uint64_t)rank < k)
            cellCounts[rank]++;
    }

    // Count collisions: for each cell with count c, collisions += c - 1 (if c > 0)
    // Equivalently: collisions = n - (number of occupied cells)
    collisions = 0;
    {
        uint64_t occupied = 0;
        for (i = 0; i < k; i++)
        {
            if (cellCounts[i] > 0)
                occupied++;
        }
        collisions = n - occupied;
    }

    // Expected collisions: E[C] = n - k*(1 - exp(-n/k))
    expectedCollisions = (double)n - (double)k * (1.0 - exp(-(double)n / (double)k));

    // Variance from balls-into-bins:
    // Var[C] = k * [(1 - 1/k)^n + (n/k)*exp(-n/k) - (1 - (1-1/k)^n)^2 / k]
    // Simplified approximation:
    // Var = k * exp(-n/k) * (1 - (1 + n/k)*exp(-n/k))
    {
        double lambda = (double)n / (double)k;
        double expNegLambda = exp(-lambda);
        variance = (double)k * expNegLambda * (1.0 - (1.0 + lambda) * expNegLambda);
    }

    // Z-score
    if (variance > 0.0)
        zScore = ((double)collisions - expectedCollisions) / sqrt(variance);
    else
        zScore = 0.0;

    privateData->zScore = zScore;

    // P-value via erfc(|Z|/sqrt(2))
    privateData->probabilityValue = erfc(fabs(zScore) / sqrt(2.0));

    STEER_FreeMemory((void**)&values);
    STEER_FreeMemory((void**)&cellCounts);

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);
    return result;
}

