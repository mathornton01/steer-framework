#define TEST_DESCRIPTION \
"The Hamming Correlation test (sstring_HammingCorr) divides the bitstream into non-overlapping \
blocks and computes the Hamming weight of each block. It then measures the correlation between \
the Hamming weights of consecutive blocks. Under the null hypothesis of independence, the \
correlation coefficient between consecutive block weights should be approximately zero. The \
test standardizes the sample correlation to a z-score and computes a two-sided p-value. \
It detects generators that produce serial dependencies in the weight structure of consecutive \
output words."
