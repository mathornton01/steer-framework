// =================================================================================================
//  Helper: count 1-bits in a block of the bitstream
// =================================================================================================
static uint64_t CountOnesInBlock(uint8_t* buffer, uint64_t bitOffset, uint64_t blockSize)
{
    uint64_t count = 0;
    uint64_t i;
    for (i = 0; i < blockSize; i++)
        count += buffer[bitOffset + i];
    return count;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tTestU01_HammingCorrelationPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t numBlocks = 0;
    uint64_t blockSize = privateData->blockSize;
    double mean = 0.0;
    double sumSq = 0.0;
    double sumCross = 0.0;
    double variance = 0.0;
    double r = 0.0;
    double z = 0.0;
    double pval = 0.0;
    uint64_t i;
    double* weights = NULL;

    numBlocks = privateData->bitstreamLength / blockSize;

    if (numBlocks < 10)
    {
        *passed = false;
        return result;
    }

    // Allocate weights array
    result = STEER_AllocateMemory(numBlocks * sizeof(double), (void**)&weights);
    if (result != STEER_RESULT_SUCCESS) return result;

    // Compute Hamming weight of each block
    for (i = 0; i < numBlocks; i++)
        weights[i] = (double)CountOnesInBlock(bitstreamBuffer, i * blockSize, blockSize);

    // Compute sample mean
    mean = 0.0;
    for (i = 0; i < numBlocks; i++)
        mean += weights[i];
    mean /= (double)numBlocks;

    // Compute sample variance and lag-1 autocovariance
    sumSq = 0.0;
    sumCross = 0.0;
    for (i = 0; i < numBlocks - 1; i++)
    {
        double di = weights[i] - mean;
        double di1 = weights[i + 1] - mean;
        sumSq += di * di;
        sumCross += di * di1;
    }
    // Add the last term to sumSq
    {
        double dn = weights[numBlocks - 1] - mean;
        sumSq += dn * dn;
    }

    variance = sumSq / (double)numBlocks;

    if (variance < 1e-30)
    {
        *passed = false;
        STEER_FreeMemory((void**)&weights);
        return result;
    }

    // Lag-1 autocorrelation
    r = (sumCross / (double)(numBlocks - 1)) / variance;
    privateData->correlation = r;

    // Under H0, r ~ Normal(0, 1/numBlocks) approximately
    z = r * sqrt((double)numBlocks);
    privateData->zScore = z;

    // Two-sided p-value
    pval = cephes_erfc(fabs(z) / sqrt(2.0));
    privateData->probabilityValue = pval;

    *passed = (pval >= privateData->significanceLevel);

    STEER_FreeMemory((void**)&weights);
    return result;
}

