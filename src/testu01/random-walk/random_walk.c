#define PROGRAM_NAME        "testu01_random_walk_test"
#define PROGRAM_VERSION     "0.1.0"
#define TEST_NAME           "random walk"
#define TEST_DESCRIPTION \
"The Random Walk test (swalk_RandomWalk1) converts the bitstream to a sequence of uniform \
[0,1) values, maps each to +1 (if >= 0.5) or -1 (if < 0.5), and forms a random walk \
S_k = sum of steps. Multiple walks of a given length are performed. The final positions \
of each walk are binned and compared against the expected symmetric distribution using a \
chi-squared test. Under the null hypothesis of randomness, the normalized final position \
S_n/sqrt(n) is approximately standard normal. This test detects generators whose outputs \
produce biased or overly regular walk trajectories."
#define CONFIGURATION_COUNT         1
#define TESTU01_NAME                "TestU01 Crush Battery"

#define MINIMUM_BITSTREAM_COUNT     1
#define MINIMUM_BITSTREAM_LENGTH    8192
#define MINIMUM_SIGNIFICANCE_LEVEL  0.0
#define MAXIMUM_SIGNIFICANCE_LEVEL  1.0

// Random Walk parameters
#define WALK_BITS_PER_VALUE     32
#define WALK_LENGTH_DEFAULT     1000
#define WALK_NUM_BINS           6       // Equal-probability bins under N(0,1)

// Standard normal quantiles for 6 equal-probability bins (each P=1/6)
// Boundaries: -inf, -0.9674, -0.4307, 0.0, 0.4307, 0.9674, +inf
static const double WALK_BIN_BOUNDARIES[] = { -0.9674, -0.4307, 0.0, 0.4307, 0.9674 };

