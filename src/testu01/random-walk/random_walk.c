// =================================================================================================
//  Helper: extract uniform [0,1) from bitstream
// =================================================================================================
static double ExtractUniform(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < WALK_BITS_PER_VALUE; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return (double)value / 4294967296.0;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tTestU01_RandomWalkPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t n = 0;
    uint64_t numWalks = 0;
    uint64_t walkLen = privateData->walkLength;
    uint64_t observed[WALK_NUM_BINS];
    double expected;
    double chiSquared = 0.0;
    double sigma;
    uint64_t w, s;
    int bin;

    memset(observed, 0, sizeof(observed));

    // Number of uniform values we can extract
    n = privateData->bitstreamLength / WALK_BITS_PER_VALUE;
    numWalks = n / walkLen;

    if (numWalks < 10)
    {
        *passed = false;
        return result;
    }

    sigma = sqrt((double)walkLen);

    // Perform each walk
    for (w = 0; w < numWalks; w++)
    {
        int64_t position = 0;
        double z;

        for (s = 0; s < walkLen; s++)
        {
            double u = ExtractUniform(bitstreamBuffer, (w * walkLen + s) * WALK_BITS_PER_VALUE);
            position += (u >= 0.5) ? 1 : -1;
        }

        // Normalize: z = S_n / sqrt(n) ~ N(0,1)
        z = (double)position / sigma;

        // Bin into equal-probability categories
        bin = 0;
        while (bin < WALK_NUM_BINS - 1 && z > WALK_BIN_BOUNDARIES[bin])
            bin++;
        observed[bin]++;
    }

    // Expected count per bin (equal probability)
    expected = (double)numWalks / (double)WALK_NUM_BINS;

    // Chi-squared statistic
    chiSquared = 0.0;
    for (bin = 0; bin < WALK_NUM_BINS; bin++)
    {
        double diff = (double)observed[bin] - expected;
        chiSquared += (diff * diff) / expected;
    }

    privateData->chiSquared = chiSquared;

    // P-value: df = WALK_NUM_BINS - 1 = 5
    privateData->probabilityValue = cephes_igamc(2.5, chiSquared / 2.0);

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);
    return result;
}

