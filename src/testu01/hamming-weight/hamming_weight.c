#define TEST_NAME           "hamming weight"
#define TEST_DESCRIPTION \
"The Hamming Weight test (sstring_HammingWeight) divides the bitstream into non-overlapping \
blocks of a given size and counts the number of 1-bits (Hamming weight) in each block. Under \
the null hypothesis, the Hamming weight of each block follows a Binomial(blockSize, 0.5) \
distribution, which is approximately normal for large blocks. The test bins the Hamming weights \
and applies a chi-squared goodness-of-fit test to compare the observed distribution against the \
expected binomial distribution."
