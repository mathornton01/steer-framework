// =================================================================================================
//  Helper: Standard normal CDF using erfc
// =================================================================================================
static double NormalCDF(double x)
{
    return 0.5 * erfc(-x / sqrt(2.0));
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tTestU01_HammingWeightPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t numBlocks = 0;
    uint64_t blockSize = privateData->blockSize;
    double mean = 0.0;
    double sigma = 0.0;
    uint64_t observed[HAMMING_NUM_BINS];
    double expected[HAMMING_NUM_BINS];
    double boundaries[HAMMING_NUM_BINS - 1];
    double chiSquared = 0.0;
    uint64_t df = HAMMING_NUM_BINS - 1;
    uint64_t i;
    uint64_t j;

    memset(observed, 0, sizeof(observed));
    memset(expected, 0, sizeof(expected));

    // Number of non-overlapping blocks
    numBlocks = privateData->bitstreamLength / blockSize;
    if (numBlocks < 2)
    {
        *passed = false;
        return result;
    }

    // Under H0: weight ~ Binomial(blockSize, 0.5)
    // Expected value = blockSize/2, variance = blockSize/4
    mean = (double)blockSize / 2.0;
    sigma = sqrt((double)blockSize / 4.0);

    // Bin boundaries: [0, mean-2s), [mean-2s, mean-s), [mean-s, mean),
    //                 [mean, mean+s), [mean+s, mean+2s), [mean+2s, blockSize]
    boundaries[0] = mean - 2.0 * sigma;
    boundaries[1] = mean - sigma;
    boundaries[2] = mean;
    boundaries[3] = mean + sigma;
    boundaries[4] = mean + 2.0 * sigma;

    // Compute expected counts using normal approximation of binomial CDF
    {
        double cdfValues[HAMMING_NUM_BINS - 1];
        for (i = 0; i < HAMMING_NUM_BINS - 1; i++)
            cdfValues[i] = NormalCDF((boundaries[i] - mean) / sigma);

        expected[0] = cdfValues[0] * (double)numBlocks;
        for (i = 1; i < HAMMING_NUM_BINS - 1; i++)
            expected[i] = (cdfValues[i] - cdfValues[i - 1]) * (double)numBlocks;
        expected[HAMMING_NUM_BINS - 1] = (1.0 - cdfValues[HAMMING_NUM_BINS - 2]) * (double)numBlocks;
    }

    // Count Hamming weight for each block and bin it
    for (i = 0; i < numBlocks; i++)
    {
        uint64_t weight = 0;
        uint64_t bitStart = i * blockSize;

        // Count 1-bits in this block
        for (j = 0; j < blockSize; j++)
            weight += bitstreamBuffer[bitStart + j];

        // Bin the weight
        if ((double)weight < boundaries[0])
            observed[0]++;
        else if ((double)weight < boundaries[1])
            observed[1]++;
        else if ((double)weight < boundaries[2])
            observed[2]++;
        else if ((double)weight < boundaries[3])
            observed[3]++;
        else if ((double)weight < boundaries[4])
            observed[4]++;
        else
            observed[5]++;
    }

    // Chi-squared statistic
    chiSquared = 0.0;
    for (i = 0; i < HAMMING_NUM_BINS; i++)
    {
        if (expected[i] > 0.0)
        {
            double diff = (double)observed[i] - expected[i];
            chiSquared += (diff * diff) / expected[i];
        }
    }

    privateData->chiSquared = chiSquared;

    // P-value via incomplete gamma: P = igamc(df/2, chi2/2)
    privateData->probabilityValue = cephes_igamc((double)df / 2.0,
                                                  chiSquared / 2.0);

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);

    return result;
}

