// =================================================================================================
//  Helper: Check if a block has a given period
// =================================================================================================
static bool HasPeriod(uint8_t* buffer, uint64_t bitOffset, uint64_t blockSize, uint64_t period)
{
    uint64_t i;
    for (i = period; i < blockSize; i++)
    {
        if (buffer[bitOffset + i] != buffer[bitOffset + (i % period)])
            return false;
    }
    return true;
}

// =================================================================================================
//  Helper: Find the minimum period of a block
// =================================================================================================
static uint64_t FindMinPeriod(uint8_t* buffer, uint64_t bitOffset, uint64_t blockSize)
{
    uint64_t p;
    for (p = 1; p < blockSize; p++)
    {
        if (blockSize % p == 0)  // only check divisors
        {
            if (HasPeriod(buffer, bitOffset, blockSize, p))
                return p;
        }
    }
    return blockSize;  // aperiodic
}

// =================================================================================================
//  Helper: Compute probability that a random block of length L is periodic
// =================================================================================================
static double ComputePeriodicProbability(uint64_t blockSize)
{
    // P(periodic) = sum over all proper divisors p of blockSize of 2^(-(blockSize - p))
    // using inclusion-exclusion this is an approximation; the dominant term is the largest
    // proper divisor.
    double prob = 0.0;
    uint64_t p;
    for (p = 1; p < blockSize; p++)
    {
        if (blockSize % p == 0)
        {
            prob += pow(2.0, -((double)(blockSize - p)));
        }
    }
    // Clamp to [0, 1]
    if (prob > 1.0)
        prob = 1.0;
    return prob;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tTestU01_PeriodsInStringsPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t blockSize = privateData->blockSize;
    uint64_t numBlocks = privateData->bitstreamLength / blockSize;
    uint64_t periodicCount = 0;
    double pPeriodic = 0.0;
    double expected = 0.0;
    double variance = 0.0;
    uint64_t i;

    if (numBlocks < 2)
    {
        *passed = false;
        return result;
    }

    // Count periodic blocks
    for (i = 0; i < numBlocks; i++)
    {
        uint64_t bitOffset = i * blockSize;
        uint64_t minPeriod = FindMinPeriod(bitstreamBuffer, bitOffset, blockSize);
        if (minPeriod < blockSize)
            periodicCount++;
    }

    // Compute expected number of periodic blocks under H0
    pPeriodic = ComputePeriodicProbability(blockSize);
    expected = (double)numBlocks * pPeriodic;
    variance = (double)numBlocks * pPeriodic * (1.0 - pPeriodic);

    // Normal approximation z-score
    if (variance > 0.0)
        privateData->zScore = ((double)periodicCount - expected) / sqrt(variance);
    else
        privateData->zScore = 0.0;

    // Two-sided p-value
    privateData->probabilityValue = cephes_erfc(fabs(privateData->zScore) / sqrt(2.0));

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);

    return result;
}

