// =================================================================================================
//  Helper: extract integer from bitstream
// =================================================================================================
static uint32_t ExtractBits(uint8_t* buffer, uint64_t bitOffset, int numBits)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < numBits; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  Helper: comparison for qsort
// =================================================================================================
static int CompareUint32(const void* a, const void* b)
{
    uint32_t va = *(const uint32_t*)a;
    uint32_t vb = *(const uint32_t*)b;
    if (va < vb) return -1;
    if (va > vb) return 1;
    return 0;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_BirthdaySpacingsPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint32_t birthdays[BDAY_M];
    uint32_t spacings[BDAY_M];
    int i;
    int duplicates = 0;
    double lambda;
    uint64_t maxBirthdays;

    // Determine how many birthdays we can extract
    maxBirthdays = privateData->bitstreamLength / BDAY_BITS;
    if (maxBirthdays > BDAY_M)
        maxBirthdays = BDAY_M;

    // Extract random birthdays from bitstream
    for (i = 0; i < (int)maxBirthdays; i++)
        birthdays[i] = ExtractBits(bitstreamBuffer, (uint64_t)i * BDAY_BITS, BDAY_BITS);

    // Sort birthdays
    qsort(birthdays, (size_t)maxBirthdays, sizeof(uint32_t), CompareUint32);

    // Compute spacings (including wrap-around)
    for (i = 0; i < (int)maxBirthdays - 1; i++)
        spacings[i] = birthdays[i + 1] - birthdays[i];
    spacings[maxBirthdays - 1] = birthdays[0] + BDAY_N - birthdays[maxBirthdays - 1];

    // Sort spacings
    qsort(spacings, (size_t)maxBirthdays, sizeof(uint32_t), CompareUint32);

    // Count duplicate spacings
    duplicates = 0;
    for (i = 1; i < (int)maxBirthdays; i++)
    {
        if (spacings[i] == spacings[i - 1])
            duplicates++;
    }

    // Expected: Poisson with lambda = m^3 / (4n)
    lambda = (double)maxBirthdays * (double)maxBirthdays * (double)maxBirthdays / (4.0 * BDAY_N);
    privateData->lambda = lambda;
    privateData->duplicateSpacings = duplicates;

    // P-value: P(X <= j) for Poisson(lambda) using incomplete gamma
    // P(X <= k) = cephes_igamc(k+1, lambda)
    privateData->probabilityValue = cephes_igamc((double)(duplicates + 1), lambda);

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);
    return result;
}

