#define PROGRAM_NAME        "diehard_birthday_spacings_test"
#define PROGRAM_VERSION     "0.1.0"
#define TEST_NAME           "birthday spacings"
#define TEST_DESCRIPTION \
"The Birthday Spacings test is based on the birthday paradox. It chooses m random \
integers (birthdays) from [0, n), sorts them, computes the spacings between \
consecutive values, and counts the number of duplicate spacings. Under the hypothesis \
of randomness, the number of duplicate spacings should follow a Poisson distribution \
with parameter lambda = m^3 / (4n). This test detects generators that fail to produce \
sufficiently independent random values."
#define CONFIGURATION_COUNT         1
#define DIEHARD_NAME                "Diehard Statistical Test Battery"

#define MINIMUM_BITSTREAM_COUNT     1
#define MINIMUM_BITSTREAM_LENGTH    32768
#define MINIMUM_SIGNIFICANCE_LEVEL  0.0
#define MAXIMUM_SIGNIFICANCE_LEVEL  1.0

// Birthday spacings parameters
#define BDAY_M      512         // Number of birthdays
#define BDAY_BITS   24          // Bits per birthday
#define BDAY_N      16777216    // 2^24 = year length

