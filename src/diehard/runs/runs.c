// =================================================================================================
//  Run length probabilities (Knuth)
//  P(length = k) = k / (k+1)! for ascending runs
//  P(1) = 1/2, P(2) = 1/3, P(3) = 1/8, P(4) = 1/30, P(5) = 1/144, P(>=6) = 1/720
//  These sum to exactly 1.
// =================================================================================================
static const double gRunLengthProb[MAX_RUN_LENGTH] = {
    1.0 / 2.0,         // P(length 1) = 1/2
    1.0 / 3.0,         // P(length 2) = 1/3
    1.0 / 8.0,         // P(length 3) = 1/8
    1.0 / 30.0,        // P(length 4) = 1/30
    1.0 / 144.0,       // P(length 5) = 1/144
    1.0 / 720.0        // P(length >= 6) = 1/720
};

// =================================================================================================
//  Helper: extract a 32-bit unsigned integer from bitstream
// =================================================================================================
static uint32_t ExtractUint32(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < 32; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_RunsPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t totalBits = privateData->bitstreamLength;
    uint64_t numValues = totalBits / 32;
    double chiSquared = 0.0;
    int i;

    uint64_t upRuns[MAX_RUN_LENGTH];
    uint64_t downRuns[MAX_RUN_LENGTH];
    double* values = NULL;

    memset(upRuns, 0, sizeof(upRuns));
    memset(downRuns, 0, sizeof(downRuns));

    if (numValues < 2)
    {
        privateData->chiSquared = 0.0;
        privateData->probabilityValue = 0.0;
        privateData->numTotalRuns = 0;
        *passed = false;
        return result;
    }

    // Convert bitstream to uniform doubles in [0,1)
    result = STEER_AllocateMemory(numValues * sizeof(double), (void**)&values);
    if (result != STEER_RESULT_SUCCESS)
        return result;

    for (i = 0; i < (int)numValues; i++)
    {
        uint32_t raw = ExtractUint32(bitstreamBuffer, (uint64_t)i * 32);
        values[i] = (double)raw / 4294967296.0;
    }

    // Count ascending runs
    {
        uint64_t runLen = 1;
        for (i = 1; i < (int)numValues; i++)
        {
            if (values[i] > values[i - 1])
                runLen++;
            else
            {
                int bin = (runLen >= 6) ? 5 : (int)(runLen - 1);
                upRuns[bin]++;
                runLen = 1;
            }
        }
        // Don't forget last run
        {
            int bin = (runLen >= 6) ? 5 : (int)(runLen - 1);
            upRuns[bin]++;
        }
    }

    // Count descending runs
    {
        uint64_t runLen = 1;
        for (i = 1; i < (int)numValues; i++)
        {
            if (values[i] < values[i - 1])
                runLen++;
            else
            {
                int bin = (runLen >= 6) ? 5 : (int)(runLen - 1);
                downRuns[bin]++;
                runLen = 1;
            }
        }
        // Don't forget last run
        {
            int bin = (runLen >= 6) ? 5 : (int)(runLen - 1);
            downRuns[bin]++;
        }
    }

    // Combine ascending and descending runs
    {
        uint64_t totalRuns[MAX_RUN_LENGTH];
        uint64_t numTotalRuns = 0;

        for (i = 0; i < MAX_RUN_LENGTH; i++)
        {
            totalRuns[i] = upRuns[i] + downRuns[i];
            numTotalRuns += totalRuns[i];
        }

        privateData->numTotalRuns = (int32_t)numTotalRuns;

        if (numTotalRuns == 0)
        {
            privateData->chiSquared = 0.0;
            privateData->probabilityValue = 0.0;
            *passed = false;
            STEER_FreeMemory((void**)&values);
            return result;
        }

        // Chi-squared: sum (observed - expected)^2 / expected
        chiSquared = 0.0;
        for (i = 0; i < MAX_RUN_LENGTH; i++)
        {
            double expected = (double)numTotalRuns * gRunLengthProb[i];
            if (expected > 0.0)
            {
                double diff = (double)totalRuns[i] - expected;
                chiSquared += (diff * diff) / expected;
            }
        }
    }

    privateData->chiSquared = chiSquared;

    // P-value using upper incomplete gamma function, df = MAX_RUN_LENGTH - 1 = 5
    privateData->probabilityValue = cephes_igamc(5.0 / 2.0, chiSquared / 2.0);

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);

    STEER_FreeMemory((void**)&values);
    return result;
}

