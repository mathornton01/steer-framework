#define TEST_NAME           "runs"
#define TEST_DESCRIPTION \
"Generates a sequence of random floats on [0,1) and counts ascending and descending runs. \
A run is a maximal sequence of consecutive values that are monotonically increasing \
(ascending run) or decreasing (descending run). The observed distribution of run lengths \
is compared against the theoretical distribution using chi-squared. NOTE: From the \
original Dieharder suite — this test is considered reliable."
