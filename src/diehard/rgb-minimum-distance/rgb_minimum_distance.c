// =================================================================================================
//  Helper: extract a 32-bit unsigned integer from bitstream
// =================================================================================================
static uint32_t ExtractUint32(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < 32; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_RgbMinDistPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t d = privateData->dimension;
    uint64_t N = privateData->numPoints;
    uint64_t totalBits = privateData->bitstreamLength;
    uint64_t bitsPerPoint = d * 32;
    uint64_t maxPoints = totalBits / bitsPerPoint;
    double* points = NULL;
    double minDistSq = 1e30;
    double pvalue = 0.0;
    uint64_t i = 0;
    uint64_t j = 0;
    uint64_t k = 0;

    if (maxPoints < N)
        N = maxPoints;

    if (N < 2)
    {
        privateData->probabilityValue = 0.0;
        *passed = false;
        return result;
    }

    // Allocate N*d doubles for point coordinates
    result = STEER_AllocateMemory(N * d * sizeof(double), (void**)&points);
    if (result != STEER_RESULT_SUCCESS)
        return result;

    // Fill points from bitstream
    for (i = 0; i < N; i++)
    {
        for (k = 0; k < d; k++)
        {
            uint32_t raw = ExtractUint32(bitstreamBuffer, (i * d + k) * 32);
            points[i * d + k] = ((double)raw + 0.5) / 4294967296.0;
        }
    }

    // Find minimum squared Euclidean distance (O(N^2) pairwise)
    for (i = 0; i < N - 1; i++)
    {
        for (j = i + 1; j < N; j++)
        {
            double distSq = 0.0;
            for (k = 0; k < d; k++)
            {
                double diff = points[i * d + k] - points[j * d + k];
                distSq += diff * diff;
            }
            if (distSq < minDistSq)
                minDistSq = distSq;
        }
    }

    STEER_FreeMemory((void**)&points);

    // Compute p-value
    // Volume of d-ball: V_d = pi^(d/2) / Gamma(d/2 + 1)
    // For the minimum distance among N uniform points:
    // P(min_dist^d < x) ~ 1 - exp(-N*(N-1)/2 * V_d * x)
    // where x = minDist^d
    {
        double minDist = sqrt(minDistSq);
        double minDistPowD = pow(minDist, (double)d);
        double halfD = (double)d / 2.0;
        double vd = pow(M_PI, halfD) / exp(lgamma(halfD + 1.0));
        double nPairs = (double)N * ((double)N - 1.0) / 2.0;
        double lambda = nPairs * vd;
        pvalue = 1.0 - exp(-lambda * minDistPowD);
    }

    // Clamp p-value
    if (pvalue < 0.0)
        pvalue = 0.0;
    if (pvalue > 1.0)
        pvalue = 1.0;

    privateData->probabilityValue = pvalue;
    *passed = (pvalue >= privateData->significanceLevel);
    return result;
}

