// =================================================================================================
//  Helper: extract a 32-bit unsigned integer from bitstream
// =================================================================================================
static uint32_t ExtractUint32(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < 32; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_3dSpherePrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t N = privateData->numPoints;
    uint64_t totalBits = privateData->bitstreamLength;
    uint64_t bitsNeeded = N * 3 * 32;
    double* points = NULL;
    uint64_t i;
    uint64_t j;
    double minDistSq = 1e30;
    double minDist;
    double r3;
    double volume;
    double lambda;
    double p;

    // Clamp N if not enough bits
    if (bitsNeeded > totalBits)
        N = totalBits / (3 * 32);

    result = STEER_AllocateMemory(sizeof(double) * N * 3, (void**)&points);

    if (result == STEER_RESULT_SUCCESS)
    {
        // Generate N points in 3D, each coordinate in [0, CUBE_EDGE)
        for (i = 0; i < N; i++)
        {
            uint64_t base = i * 3 * 32;
            uint32_t rx = ExtractUint32(bitstreamBuffer, base);
            uint32_t ry = ExtractUint32(bitstreamBuffer, base + 32);
            uint32_t rz = ExtractUint32(bitstreamBuffer, base + 64);
            points[i * 3 + 0] = (double)rx * CUBE_EDGE / 4294967296.0;
            points[i * 3 + 1] = (double)ry * CUBE_EDGE / 4294967296.0;
            points[i * 3 + 2] = (double)rz * CUBE_EDGE / 4294967296.0;
        }

        // Find minimum squared distance between any pair (O(N^2))
        for (i = 0; i < N - 1; i++)
        {
            for (j = i + 1; j < N; j++)
            {
                double dx = points[i * 3 + 0] - points[j * 3 + 0];
                double dy = points[i * 3 + 1] - points[j * 3 + 1];
                double dz = points[i * 3 + 2] - points[j * 3 + 2];
                double d2 = dx * dx + dy * dy + dz * dz;
                if (d2 < minDistSq)
                    minDistSq = d2;
            }
        }

        minDist = sqrt(minDistSq);
        r3 = minDist * minDist * minDist;

        // For N points in a cube of volume V = CUBE_EDGE^3:
        // lambda = N*(N-1)/2 * (4*pi/3) / V
        volume = CUBE_EDGE * CUBE_EDGE * CUBE_EDGE;
        lambda = (double)N * (double)(N - 1) / 2.0 * (4.0 * M_PI / 3.0) / volume;

        // p = 1 - exp(-lambda * r3)
        p = 1.0 - exp(-lambda * r3);

        // Clamp p to [0,1]
        if (p < 0.0)
            p = 0.0;
        if (p > 1.0)
            p = 1.0;

        privateData->probabilityValue = p;
    }

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);

    STEER_FreeMemory((void**)&points);

    return result;
}

