// =================================================================================================
//  Helper: extract a 32-bit unsigned integer from bitstream
// =================================================================================================
static uint32_t ExtractUint32(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < 32; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_DabDctPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t totalBits = privateData->bitstreamLength;
    uint64_t N = privateData->blockSize;
    uint64_t bitsPerValue = 32;
    uint64_t totalValues = totalBits / bitsPerValue;
    uint64_t numBlocks = totalValues / N;
    double chiSquared = 0.0;
    uint64_t df = 0;
    double* block = NULL;
    uint64_t b = 0;
    uint64_t k = 0;
    uint64_t n = 0;

    // Allocate block
    result = STEER_AllocateMemory(N * sizeof(double), (void**)&block);

    if (result == STEER_RESULT_SUCCESS)
    {
        for (b = 0; b < numBlocks; b++)
        {
            // Fill block with uniform [0,1) values from 32-bit integers
            for (n = 0; n < N; n++)
            {
                uint32_t raw = ExtractUint32(bitstreamBuffer, (b * N + n) * 32);
                block[n] = ((double)raw + 0.5) / 4294967296.0;
            }

            // DCT-II for each non-DC coefficient (skip k=0)
            for (k = 1; k < N; k++)
            {
                double Y = 0.0;
                for (n = 0; n < N; n++)
                    Y += block[n] * cos(M_PI * (2.0 * n + 1.0) * k / (2.0 * N));

                // For uniform [0,1) input, E[Y] = 0, Var[Y] = N/24
                // Standardize: z = Y / sqrt(N/24)
                {
                    double z = Y / sqrt((double)N / 24.0);
                    chiSquared += z * z;
                    df++;
                }
            }
        }

        STEER_FreeMemory((void**)&block);
    }

    privateData->chiSquared = chiSquared;

    // P-value using upper incomplete gamma function
    if (df > 1)
        privateData->probabilityValue = cephes_igamc((double)df / 2.0, chiSquared / 2.0);
    else
        privateData->probabilityValue = 0.0;

    *passed = (privateData->probabilityValue >= privateData->significanceLevel);
    return result;
}

