// =================================================================================================
//  Helper: compute factorial
// =================================================================================================
static uint64_t Factorial(uint64_t n)
{
    uint64_t f = 1;
    uint64_t i;
    for (i = 2; i <= n; i++)
        f *= i;
    return f;
}

// =================================================================================================
//  Helper: compute Lehmer code (permutation rank) for a group of t values
//  Returns a number in [0, t!)
// =================================================================================================
static uint64_t PermutationRank(double* values, uint64_t t)
{
    uint64_t rank = 0;
    uint64_t i;
    uint64_t j;
    uint64_t fact;

    for (i = 0; i < t - 1; i++)
    {
        uint64_t count = 0;
        for (j = i + 1; j < t; j++)
        {
            if (values[j] < values[i])
                count++;
        }
        fact = Factorial(t - 1 - i);
        rank += count * fact;
    }
    return rank;
}

// =================================================================================================
//  Helper: extract a 32-bit unsigned integer from bitstream
// =================================================================================================
static uint32_t ExtractUint32(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < 32; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_RgbPermutationsPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t t = privateData->tupleSize;
    uint64_t numPerms = Factorial(t);
    uint64_t totalBits = privateData->bitstreamLength;
    uint64_t totalValues = totalBits / 32;
    uint64_t numGroups = totalValues / t;
    uint64_t* observed = NULL;
    double* group = NULL;
    double expected = 0.0;
    double chiSquared = 0.0;
    uint64_t df = 0;
    uint64_t i = 0;
    uint64_t j = 0;

    result = STEER_AllocateMemory(numPerms * sizeof(uint64_t), (void**)&observed);
    if (result == STEER_RESULT_SUCCESS)
    {
        result = STEER_AllocateMemory(t * sizeof(double), (void**)&group);
    }

    if (result == STEER_RESULT_SUCCESS)
    {
        memset(observed, 0, numPerms * sizeof(uint64_t));

        for (i = 0; i < numGroups; i++)
        {
            for (j = 0; j < t; j++)
            {
                uint32_t raw = ExtractUint32(bitstreamBuffer, (i * t + j) * 32);
                group[j] = ((double)raw + 0.5) / 4294967296.0;
            }
            {
                uint64_t rank = PermutationRank(group, t);
                if (rank < numPerms)
                    observed[rank]++;
            }
        }

        STEER_FreeMemory((void**)&group);

        expected = (double)numGroups / (double)numPerms;
        chiSquared = 0.0;
        for (j = 0; j < numPerms; j++)
        {
            double diff = (double)observed[j] - expected;
            chiSquared += (diff * diff) / expected;
        }

        STEER_FreeMemory((void**)&observed);

        df = numPerms - 1;
        privateData->chiSquared = chiSquared;
        privateData->probabilityValue = cephes_igamc((double)df / 2.0, chiSquared / 2.0);
        *passed = (privateData->probabilityValue >= privateData->significanceLevel);
    }
    else
    {
        STEER_FreeMemory((void**)&observed);
        STEER_FreeMemory((void**)&group);
    }

    return result;
}

