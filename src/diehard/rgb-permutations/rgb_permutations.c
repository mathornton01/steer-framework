#define TEST_NAME           "rgb permutations"
#define TEST_DESCRIPTION \
"Tests whether the orderings of successive non-overlapping groups of random numbers occur \
with equal probability among all possible permutations. Unlike Diehard's overlapping \
permutation test (operm5), this uses non-overlapping windows to avoid covariance problems. \
For groups of size t, there are t! possible orderings, each expected with equal probability. \
A chi-squared test compares observed vs expected permutation frequencies. NOTE: From the \
original Dieharder suite -- this test is considered reliable and avoids the known issues \
with the overlapping version."
