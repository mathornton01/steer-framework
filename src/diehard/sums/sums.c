// =================================================================================================
//  Helper: extract a 32-bit unsigned integer from bitstream
// =================================================================================================
static uint32_t ExtractUint32(uint8_t* buffer, uint64_t bitOffset)
{
    uint32_t value = 0;
    int i;
    for (i = 0; i < 32; i++)
        value = (value << 1) | buffer[bitOffset + i];
    return value;
}

// =================================================================================================
//  Helper: compare doubles for qsort
// =================================================================================================
static int CompareDoubles(const void* a, const void* b)
{
    double da = *(const double*)a;
    double db = *(const double*)b;
    if (da < db) return -1;
    if (da > db) return 1;
    return 0;
}

// =================================================================================================
//  Helper: KS p-value using asymptotic formula
// =================================================================================================
static double KSPvalue(double D, uint64_t N)
{
    double sqrtN = sqrt((double)N);
    double lambda = (sqrtN + 0.12 + 0.11 / sqrtN) * D;
    double lambda2 = lambda * lambda;
    double sum = 0.0;
    int k;
    double sign = 1.0;

    for (k = 1; k <= 100; k++)
    {
        double term = sign * exp(-2.0 * (double)k * (double)k * lambda2);
        sum += term;
        sign = -sign;
        if (fabs(term) < 1e-12) break;
    }

    double pvalue = 2.0 * sum;
    if (pvalue < 0.0) pvalue = 0.0;
    if (pvalue > 1.0) pvalue = 1.0;
    return pvalue;
}

// =================================================================================================
//  RunTest
// =================================================================================================
int32_t RunTest(tDiehard_SumsPrivateData* privateData,
                uint8_t* bitstreamBuffer,
                bool* passed)
{
    int32_t result = STEER_RESULT_SUCCESS;
    uint64_t totalBits = privateData->bitstreamLength;
    uint64_t totalValues = totalBits / 32;
    uint64_t numSums = 0;
    double* values = NULL;
    double* pvalues = NULL;
    double Dplus = 0.0;
    double Dminus = 0.0;
    double D;
    uint64_t i;

    if (totalValues < SUM_LENGTH)
    {
        privateData->ksStatistic = 0.0;
        privateData->probabilityValue = 0.0;
        *passed = false;
        return result;
    }

    numSums = totalValues - (SUM_LENGTH - 1);

    // Allocate arrays for uniform values and p-values
    result = STEER_AllocateMemory(totalValues * sizeof(double), (void**)&values);

    if (result == STEER_RESULT_SUCCESS)
        result = STEER_AllocateMemory(numSums * sizeof(double), (void**)&pvalues);

    if (result == STEER_RESULT_SUCCESS)
    {
        double sumMean = (double)SUM_LENGTH * 0.5;             // 50.0
        double sumStddev = sqrt((double)SUM_LENGTH / 12.0);    // sqrt(100/12)

        // Convert 32-bit integers to uniform [0,1) doubles
        for (i = 0; i < totalValues; i++)
        {
            uint32_t raw = ExtractUint32(bitstreamBuffer, i * 32);
            values[i] = ((double)raw + 0.5) / 4294967296.0;
        }

        // Compute first overlapping sum: values[0] + ... + values[99]
        double currentSum = 0.0;
        for (i = 0; i < SUM_LENGTH; i++)
            currentSum += values[i];

        // Standardize and convert to p-value via normal CDF
        double z = (currentSum - sumMean) / sumStddev;
        pvalues[0] = 0.5 * cephes_erfc(-z / sqrt(2.0));

        // Compute remaining overlapping sums using sliding window
        for (i = 1; i < numSums; i++)
        {
            currentSum = currentSum - values[i - 1] + values[i + SUM_LENGTH - 1];
            z = (currentSum - sumMean) / sumStddev;
            pvalues[i] = 0.5 * cephes_erfc(-z / sqrt(2.0));
        }

        // Sort p-values for KS test
        qsort(pvalues, (size_t)numSums, sizeof(double), CompareDoubles);

        // Compute KS statistic: D+, D-
        for (i = 0; i < numSums; i++)
        {
            double Fn = (double)(i + 1) / (double)numSums;
            double Fn_prev = (double)i / (double)numSums;
            double dp = Fn - pvalues[i];
            double dm = pvalues[i] - Fn_prev;
            if (dp > Dplus) Dplus = dp;
            if (dm > Dminus) Dminus = dm;
        }

        D = (Dplus > Dminus) ? Dplus : Dminus;

        privateData->ksStatistic = D;
        privateData->probabilityValue = KSPvalue(D, numSums);
        *passed = (privateData->probabilityValue >= privateData->significanceLevel);
    }

    STEER_FreeMemory((void**)&values);
    STEER_FreeMemory((void**)&pvalues);

    return result;
}

